using System;

namespace VariablesInC_
{
    internal class Program
    {
        static void Main(string[] args)
        {
            // Here I define a variable that stores a name
            string name = "Jenny";
            Console.WriteLine(name = "Jenny");

            // Here I define a variable that stores a number
            int num = 1;
            Console.WriteLine(num = 1);

            //  Here I define another variable with a different number
            int num2 = 2;
            Console.WriteLine(num = 2);
            
        }
    }
}
